    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        produtoDAO bd;
	Produto p;

	List<Produto> listaPrduto;
	bd = new produtoDAO();
	p = new Produto();
	listaPrduto = bd.consultarProdutoPorNome(txtconsultaprod.getText());

	DefaultTableModel model = (DefaultTableModel)
	jTproduto.getModel();

	model.setNumRows(0);

	for( int itemLista = 0; itemLista < listaPrduto.size(); itemLista++){
		p = listaPrduto.get(itemLista);
		model.addRow(new Object[] { p.getCodigo(), p.getNome(), p.getMarca(), p.getPreco() });
	}
    }//GEN-LAST:event_jButton1ActionPerformed

